   private void novoFuncionario(){
            

        Funcionario f = new Funcionario();

       
        f.setNome(nomeFuncionario.getText());
             
        f.setSenha(senhaCa.getText());
       
        
        
        
            FuncionarioDAO dao;
            try {
                dao = new FuncionarioDAO();
                 dao.adicionaFuncionario(f);
            } catch (SQLException ex) {
                Logger.getLogger(FuncionarioFu.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");  
           
      }

